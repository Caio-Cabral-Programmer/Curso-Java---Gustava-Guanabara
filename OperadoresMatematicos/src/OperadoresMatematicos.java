public class OperadoresMatematicos {
    public static void main(String[] args) {
        // Math.PI
        System.out.println(Math.PI);

        // Math.pow(2, 3)
        System.out.println(Math.pow(2, 3));

        // Math.sqrt(16)
        System.out.println(Math.sqrt(16));

        // Math.cbrt(27)
        System.out.println(Math.cbrt(27));

        // Math.abs(-10)
        System.out.println(Math.abs(-10));

        // Math.round(3.7)
        System.out.println(Math.round(3.7));

        // Math.floor(3.7)
        System.out.println(Math.floor(3.7));

        // Math.ceil(3.7)
        System.out.println(Math.ceil(3.7));

        // Math.random() número aleatório entre 10 e 20
        System.out.println(Math.random() * 10 + 10);

        // Math.random() número inteiro aleatório entre 10 e 20
        System.out.println((int)(Math.random() * 10 + 10));

        // Math.random() número aleatório entre 0 e 1
        System.out.println(Math.random());

    }
}
