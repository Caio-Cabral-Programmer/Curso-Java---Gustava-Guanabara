public class OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 5;
        numero++;
        System.out.println(numero);
        numero--;
        System.out.println(numero);
        numero *= 2;
        System.out.println(numero);
        numero /= 3;
        System.out.println(numero);
        numero %= 4;
        System.out.println(numero);
        numero &= 5;
        System.out.println(numero);
        numero |= 6;
        System.out.println(numero);
        numero ^= 7;
        System.out.println(numero);
        numero >>= 8;
        System.out.println(numero);
        numero <<= 9;
        System.out.println(numero);
    }
}
