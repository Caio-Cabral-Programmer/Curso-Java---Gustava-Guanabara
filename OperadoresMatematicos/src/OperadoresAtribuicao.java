public class OperadoresAtribuicao {
    public static void main(String[] args) {
        // Adição
        int x = 5;
        x += 3;
        System.out.println(x);

        // Subtração
        x -= 2;
        System.out.println(x);

        // Multiplicação
        x *= 4;
        System.out.println(x);

        // Divisão
        x /= 2;
        System.out.println(x);

        //Resto
        x %= 3;
        System.out.println(x);
}}

